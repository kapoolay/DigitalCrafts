function lvl1exercise1 () {
  // Declare a variable without instantiating it and return it!
  let myVar;          // declared myVar as a variable without assigning a value
  return myVar;       // returns myVar on the console
}

function lvl1exercise2 () {
  // Declare and instantiate a number and return it
  let num = 12;         // declares num as 12;
  return num;           // returns 12
}

function lvl1exercise3 () {
  // Declare and instantiate a floating point number that is not a whole number and return it
  let myFlt = .314;         // declares myFlt as a float and assiging .314 as its value
  return myflt;             // returns .314
}

function lvl1exercise4 () {
  // Declare and instantiate a string "Hello World!" and return it
  let greet = 'Hello World!';         // declares greet as a variable and assigning 'Hello World!' as its value
  return greet;                       // returns 'Hello World!'
}

function lvl1exercise5 () {
  // Declare and instantiate an array containing the string "Hello World!" and the number 4 and return it
  let arr = ['Hello World!', 4];          // declares arr as an array and assigning 'Hello World!' and 4 as values
  return arr;                             // returns ['Hello World!', 4]
}

function lvl1exercise6 () {
  // Declare and instantiate an object containing the key-value pairs key1 -> "Hello World!" and key2 -> 4, and return it
  let myObj = {                   // declares myObj as an object
    key1: 'Hello World!',         // assigns keys and values to the object
    key2: 4,
  }
  return myObj;                   // myObj
}

function lvl1exercise7 () {
  // Declare and instantiate a boolean value 'false' and return it
  let myBoo = false;          // declares myBoo as a boolean that is false
  return myBoo;               // returns false
}
