function lvl2exercise1 (num1, num2) {
  // Return the sum of num1 and num2
  return num1 + num2;         // returns the sum of both arguments
}

function lvl2exercise2 (num1, num2) {
  // Return the difference of num1 and num2
  return num1 - num2;         // returns the difference of both arguments
}

function lvl2exercise3 (num1, num2) {
  // Return the multiplication of num1 and num 2
  return num1 * num2;         // returns the value when both arguments are multiplied
}

function lvl2exercise4 (num1, num2) {
  // Return the division of num1 and num2
  return num1 / num2;         // returns the value when both arguments are divided
}

function lvl2exercise5 (num1) {
  // Add 2 to num1 using += and return it
  num1 += 2;          // another way of saying num1 = num1 + 2;
  return num1;        // returns the value of num1 + 2
}
